# ===== NUMBERS =====
print(3 + 2)    # 5
print(10 / 3)   # 3.333... (float division)
print(10 // 3)  # 3 (floor division)
print(10 % 3)   # 1 (remainder)
print(2 ** 8)   # 256 (power)

# ===== BOOLEANS =====
print(True and False)   # False
print(True or False)    # True
print(not True)         # False

# ===== COMPARISONS =====
print(1 == 1)   # True
print(2 != 3)   # True
print(1 < 10)   # True

# ===== STRINGS =====
name = "Arya"
print(f"Hello, {name}!")          # f-string
print("Python"[0])                # P (indexing)
print("Python"[2:5])              # tho (slicing)
print("hello".upper())            # HELLO
print(len("hello"))               # 5
print("Arya" + " is a pro")